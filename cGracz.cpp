CGracz::CGracz(char z)
{
	this->znak = z;
	this->wygrane = 0;
}
//---------------------------------------------------------------------------
void CGracz::PodbijIloscWygranych(int i)
{
	this->wygrane += i;
}
//---------------------------------------------------------------------------
int CGracz::PobierzIloscWygranych()
{
	return this->wygrane;
}
//---------------------------------------------------------------------------
char CGracz::PobierzZnak()
{
    return this->znak;
}
